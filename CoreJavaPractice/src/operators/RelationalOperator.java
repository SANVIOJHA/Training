package operators;


public class RelationalOperator {

	public static void main(String[] args) {
		int a=7;
		int b=90;
	
		System.out.println("a>b "+(a>b));
		System.out.println("a>=b "+(a>=b));
		System.out.println("a<=b "+(a<=b));
		System.out.println("a!=b "+(a!=b));
		System.out.println("a==b "+(a==b));
	}

}
