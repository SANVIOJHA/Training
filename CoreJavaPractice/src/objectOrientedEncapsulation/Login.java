package objectOrientedEncapsulation;
//MainL
public class Login {
	private String name;
	private String pass;
	//user name getter and setter
	public String getName() {
		
		return this.name;
	}
	
	public void setName(String name) {
		this.name=name;
	}
	
	//user password getter and setter
		public String getPass() {
			return this.pass;
		}
		
		public void setPass(String pass) {
			this.pass=pass;
		}
	
		

}
